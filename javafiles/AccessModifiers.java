public class AccessModifiers {
    //classes can be outerclass and innerclass
    // for outerclass, there can be only two possibilities, either class is a public class or just a class which means it is of default type.
    // for innerclass any among four access modifiers can be used.

    // meaning of access modifiers :-
    // 1. private - when members of the class are private, they can not accessed from outside the class body. They are meant to be accessed from the same class in which they are declared.

    // 2 - protected - When members are protected, they can be accessed from any class of the same package and child class from other package.

    // 3 - public - When members are public, they are accessible from any class.

    // 4 - default - When members are default, they are accessible only from the class of the same package.
}
