import java.util.*;

public class Codeforces {
    
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        int t=sc.nextInt();
        if (t%2==0){
            System.out.println("YES");
        } else if(t==1) {
            System.out.println("NO");
        } else {
            System.out.println("NO");
        }
    }
}




            

                    

        

            
        
